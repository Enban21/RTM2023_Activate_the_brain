def RunTest():
    manager = OpenRTM_aist.Manager.instance()
    comp = manager.getComponent("SendTest0")
    if comp is None:
        print('Component get failed.', file=sys.stderr)
        return False
    return comp.runTest()

def SendTestInit(manager):
    profile = OpenRTM_aist.Properties(defaults_str=sendtest_spec)
    manager.registerFactory(profile,
                            SendTest,
                            OpenRTM_aist.Delete)

def MyModuleInit(manager):
    SendTestInit(manager)
    Send.SendInit(manager)

    # Create a component
    comp = manager.createComponent("SendTest")

def main():
    mgr = OpenRTM_aist.Manager.init(sys.argv)
    mgr.setModuleInitProc(MyModuleInit)
    mgr.activateManager()
    mgr.runManager(True)

    ret = RunTest()
    mgr.shutdown()

    if ret:
        sys.exit(0)
    else:
        sys.exit(1)

if __name__ == "__main__":
    main()

